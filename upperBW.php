      <p id="text">Brotowali termasuk dalam tanaman obat tradisional yang sudah dimanfaatkan selama bertahun-tahun oleh
         banyak masyarakat Indonesia untuk menyembuhkan penyakit, salah satunya adalah diabetes. <span id="btnNext">Baca Selengkapnya..</span>
         <span id="next" style="display:none"> Brotowali yang mempunyai nama latin Tinospora crispa merupakan tanaman dari keluarga Menispermiaceae. 
         Tanaman ini banyak ditemukan di Asia Tenggara dan India bagian timur laut.Tak hanya batangnya, bagian daun dan akar 
         tanaman brotowali juga bisa dimanfaatkan sebagai tanaman obat. Tanaman yang banyak tumbuh di daerah tropis ini sudah digunakan 
         sejak zaman 
         dahulu sebagai obat herba di berbagai negara, termasuk India dan Indonesia.Tanaman ini dapat dikenali dari daunnya yang berbentuk hati dan batangnya yang memiliki tonjolan-tonjolan kecil.
         Bagian batang inilah yang umumnya dimanfaatkan sebagai obat tradisional.
