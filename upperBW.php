                        <li class="list">Menurunkan Kadar Gula Darah</li>
                        <span class="captionColl">Salah satu manfaat brotowali yang sudah dikenal sejak lama adalah sebagai obat
                        herba untuk menurunkan kadar gula darah. Efek ini baik untuk mencegah terjadinya penyakit diabetes tipe 2.
                        Manfaat tersebut juga didukung oleh beberapa riset yang menunjukkan bahwa brotowali bisa memperbaiki kinerja 
                        hormon insulin dan mencegah terjadinya resistensi insulin, sehingga dapat menjaga kadar gula darah tetap stabil.</span>

                        <li class="list">Mencegah Kamuhnya Alergi</li>
                        <span class="captionColl">Ada riset yang menyebutkan bahwa konsumsi brotowali terlihat dapat mengurangi gejala alergi, 
                        seperti bersin, pilek, gatal-gatal, dan bentol-bentol di kulit.
                        Penelitian tersebut bahkan menunjukkan bahwa brotowali dapat mengurangi 
                        kambuhnya penyakit alergi, seperti rhinitis alergi dan eksim.</span>

                        <li class="list">Meningkatkan Daya Tahan Tubuh</li>
                        <span class="captionColl">Brotowali mengandung antioksidan, vitamin C, dan beragam nutrisi yang diketahui 
